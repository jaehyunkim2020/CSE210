using System.Collections.Generic;
using System.Linq;

public class Prompts
{
    public List<string> prompts = new List<string>();

    public Prompts()
    {
        prompts.Add("What was my favorite experience today?");
        prompts.Add("What was my least favorite experience today?");
        prompts.Add("If I had one thing I could do over today, what would it be?");
        prompts.Add("Who did I interact with today?");
        prompts.Add("What did I do well today?");
        prompts.Add("What could I have done better today?");
        prompts.Add("What did I accomplish today?");
        prompts.Add("What am I grateful for today?");
        prompts.Add("How did you feel throughout the day today? Did it change?");
        prompts.Add("In what way was this day different from other days?");
    }
}